/**
 * Returns true if the value passed is odd.
 * @param value
 * @returns {boolean}
 */
export const isOdd = (value: number): boolean => !(value % 2 === 0);

/**
 * Returns true if the value passed is even.
 * @param value
 * @returns {boolean}
 */
export const isEven = (value: number): boolean => value % 2 === 0;
